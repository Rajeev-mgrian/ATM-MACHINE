package ltAssignment;

public class RelationalVariables {
		 public static void main(String[] args) {
		 int a = 12;
		 double b = 8.5;

		 // Greater than operation
		 boolean isAGreaterThanB = a > b;
		 System.out.println("Is A greater than B? " + isAGreaterThanB);

		 // Less than operation
		 boolean isALessThanB = a < b;
		 System.out.println("Is A less than B? " + isALessThanB);

		 // Equal to operation
		 boolean areEqual = a == b;
		 System.out.println("Are A and B equal? " + areEqual);

		 // Not equal to operation
		 boolean areNotEqual = a != b;
		 System.out.println("Are A and B not equal? " + areNotEqual);
		 }
		}
